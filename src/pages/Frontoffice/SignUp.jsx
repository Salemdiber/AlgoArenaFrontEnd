import React, { useState, useRef, useEffect } from 'react';
import ReCAPTCHA from 'react-google-recaptcha';
import { Box, Heading, Text, Button, VStack, HStack, Input, Link, Flex, InputGroup, InputLeftElement, InputRightElement, IconButton, Icon, Grid, Image, Spinner } from '@chakra-ui/react';
import { Link as RouterLink, useNavigate } from 'react-router-dom';
import { motion } from 'framer-motion';
import AuthLayout from '../../layout/AuthLayout';
import { useAuth, redirectBasedOnRole } from './auth/context/AuthContext';
import { authService } from '../../services/authService';

const MotionBox = motion.create(Box);

const EyeIcon = (props) => (
    <Icon viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round" {...props}>
        <path d="M1 12s4-8 11-8 11 8 11 8-4 8-11 8-11-8-11-8z" />
        <circle cx="12" cy="12" r="3" />
    </Icon>
);

const EyeOffIcon = (props) => (
    <Icon viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round" {...props}>
        <path d="M17.94 17.94A10.07 10.07 0 0 1 12 20c-7 0-11-8-11-8a18.45 18.45 0 0 1 5.06-5.94M9.9 4.24A9.12 9.12 0 0 1 12 4c7 0 11 8 11 8a18.5 18.5 0 0 1-2.16 3.19m-6.72-1.07a3 3 0 1 1-4.24-4.24" />
        <line x1="1" y1="1" x2="23" y2="23" />
    </Icon>
);

const SignUp = () => {
    const [username, setUsername] = useState('');
    const [email, setEmail] = useState('');
    const [password, setPassword] = useState('');
    const [showPassword, setShowPassword] = useState(false);
    const [isLoading, setIsLoading] = useState(false);
    const [recaptchaToken, setRecaptchaToken] = useState(null);
    const [errorMsg, setErrorMsg] = useState('');
    const recaptchaRef = useRef(null);
    const { signup } = useAuth();
    const navigate = useNavigate();

    const [usernameStatus, setUsernameStatus] = useState({ state: 'idle', message: '' }); // 'idle', 'loading', 'available', 'taken'
    const [emailStatus, setEmailStatus] = useState({ state: 'idle', message: '' });

    useEffect(() => {
        if (!username || username.length < 3) {
            setUsernameStatus({ state: 'idle', message: '' });
            return;
        }

        const timeoutId = setTimeout(async () => {
            setUsernameStatus({ state: 'loading', message: '' });
            try {
                const res = await authService.checkAvailability('username', username);
                if (res.available) setUsernameStatus({ state: 'available', message: 'Available' });
                else setUsernameStatus({ state: 'taken', message: res.message || 'Taken' });
            } catch (err) {
                setUsernameStatus({ state: 'idle', message: '' });
            }
        }, 500);

        return () => clearTimeout(timeoutId);
    }, [username]);

    useEffect(() => {
        if (!email || !/^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(email)) {
            setEmailStatus({ state: 'idle', message: '' });
            return;
        }

        const timeoutId = setTimeout(async () => {
            setEmailStatus({ state: 'loading', message: '' });
            try {
                const res = await authService.checkAvailability('email', email);
                if (res.available) setEmailStatus({ state: 'available', message: 'Available' });
                else setEmailStatus({ state: 'taken', message: res.message || 'Taken' });
            } catch (err) {
                setEmailStatus({ state: 'idle', message: '' });
            }
        }, 500);

        return () => clearTimeout(timeoutId);
    }, [email]);

    const isFormInvalid = usernameStatus.state === 'taken' || emailStatus.state === 'taken' || password.length < 6;

    const handleSubmit = async (e) => {
        e.preventDefault();
        setErrorMsg('');

        if (usernameStatus.state === 'taken') {
            setErrorMsg('Username is already taken.');
            return;
        }
        if (emailStatus.state === 'taken') {
            setErrorMsg('Email is already taken.');
            return;
        }
        if (password.length < 6) {
            setErrorMsg('Password must be at least 6 characters long.');
            return;
        }

        if (!recaptchaToken) {
            setErrorMsg('Please complete reCAPTCHA to sign up.');
            return;
        }
        setIsLoading(true);
        try {
            await signup(username, email, password, recaptchaToken);
            navigate('/signin');
        } catch (err) {
            // error handled by toast in AuthContext
        } finally {
            setIsLoading(false);
        }
    };

    const strength = password.length === 0 ? { w: '0%', l: '', c: 'gray.500' }
        : password.length < 4 ? { w: '25%', l: 'Weak', c: 'red.400' }
            : password.length < 6 ? { w: '50%', l: 'Fair', c: 'yellow.400' }
                : password.length < 8 ? { w: '75%', l: 'Good', c: 'blue.400' }
                    : { w: '100%', l: 'Strong Defense', c: 'green.400' };

    const inputStyles = {
        bg: '#0f172a', border: '1px solid', borderColor: 'gray.600', borderRadius: '8px',
        color: 'gray.100', h: '48px', fontSize: 'sm', _placeholder: { color: 'gray.500' },
        _focus: { borderColor: 'brand.500', boxShadow: '0 0 0 1px #22d3ee, 0 0 20px -5px rgba(34,211,238,0.3)', outline: 'none' },
        _hover: { borderColor: 'gray.500' }, transition: 'all 0.3s',
    };



    return (
        <AuthLayout activeTab="signup">
            <MotionBox w="100%" maxW="5xl" initial={{ opacity: 0, y: 20 }} animate={{ opacity: 1, y: 0 }} transition={{ duration: 0.6 }}>
                <Box className="glass-panel" borderRadius="16px" boxShadow="0px 8px 24px -4px rgba(15,23,42,0.5)" overflow="hidden" display="flex" flexDirection={{ base: 'column', lg: 'row' }} minH="600px">

                    {/* Left Panel â€” Gamified Preview */}
                    <Box display={{ base: 'none', lg: 'flex' }} w={{ lg: '42%' }} bg="rgba(15,23,42,0.5)" position="relative" flexDirection="column" justifyContent="space-between" p={10} borderRight="1px solid" borderColor="whiteAlpha.50">
                        <Box position="absolute" inset={0} opacity={0.3} backgroundSize="40px 40px" backgroundImage="linear-gradient(to right, rgba(30,41,59,0.5) 1px, transparent 1px), linear-gradient(to bottom, rgba(30,41,59,0.5) 1px, transparent 1px)" />
                        <Box position="absolute" inset={0} bgGradient="linear(to-b, transparent, transparent, rgba(15,23,42,0.9))" />

                        <Box position="relative" zIndex={10}>
                            {/* Live badge */}
                            <HStack spacing={2} px={3} py={1} borderRadius="full" bg="rgba(34,211,238,0.1)" border="1px solid" borderColor="rgba(34,211,238,0.2)" display="inline-flex" mb={6}>
                                <Box position="relative" w={2} h={2}>
                                    <Box position="absolute" inset={0} borderRadius="full" bg="brand.500" opacity={0.75} sx={{ animation: 'ping 1s cubic-bezier(0,0,0.2,1) infinite' }} />
                                    <Box position="relative" w={2} h={2} borderRadius="full" bg="brand.500" />
                                </Box>
                                <Text fontSize="xs" fontFamily="mono" color="brand.500">LIVE ARENA</Text>
                            </HStack>
                            <Heading fontFamily="heading" fontSize="3xl" fontWeight="bold" color="white" lineHeight="tight">
                                Rise Through<br />The <Text as="span" bgGradient="linear(to-r, brand.500, blue.500)" bgClip="text" color="transparent">Ranks</Text>
                            </Heading>
                            <Text mt={4} color="gray.400" fontSize="sm" lineHeight="relaxed">Join 10,000+ developers competing daily. Solve problems, earn XP, and get scouted by top tech companies.</Text>
                        </Box>

                        {/* Stats cards */}
                        <VStack spacing={4} position="relative" zIndex={10} mt={8}>
                            <Box w="100%" bg="rgba(30,41,59,0.8)" backdropFilter="blur(8px)" border="1px solid" borderColor="gray.600" borderRadius="lg" p={4} transition="transform 0.3s" _hover={{ transform: 'scale(1.05)' }}>
                                <Flex justify="space-between" align="center" mb={2}>
                                    <Text fontSize="xs" color="gray.400" fontFamily="mono">CURRENT RANK</Text>
                                    <HStack spacing={1}>
                                        <svg width="12" height="12" viewBox="0 0 24 24" fill="none" stroke="#eab308" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round"><path d="M6 9H4.5a2.5 2.5 0 0 1 0-5H6" /><path d="M18 9h1.5a2.5 2.5 0 0 0 0-5H18" /><path d="M4 22h16" /><path d="M10 14.66V17c0 .55-.47.98-.97 1.21C7.85 18.75 7 20 7 22" /><path d="M14 14.66V17c0 .55.47.98.97 1.21C16.15 18.75 17 20 17 22" /><path d="M18 2H6v7a6 6 0 0 0 12 0V2Z" /></svg>
                                        <Text fontSize="xs" fontWeight="bold" color="yellow.400">ROOKIE III</Text>
                                    </HStack>
                                </Flex>
                                <Box w="100%" bg="gray.600" h="6px" borderRadius="full" overflow="hidden">
                                    <Box h="100%" w="75%" borderRadius="full" bgGradient="linear(to-r, brand.500, cyan.400)" boxShadow="0 0 10px rgba(34,211,238,0.5)" />
                                </Box>
                                <Flex justify="space-between" mt={1}><Text fontSize="10px" color="gray.500">0 XP</Text><Text fontSize="10px" color="gray.500">1000 XP</Text></Flex>
                            </Box>

                            <Flex w="100%" bg="rgba(30,41,59,0.8)" backdropFilter="blur(8px)" border="1px solid" borderColor="gray.600" borderRadius="lg" p={4} align="center" gap={4}>
                                <Flex w={10} h={10} borderRadius="full" bgGradient="linear(to-br, purple.500, purple.700)" align="center" justify="center" fontWeight="bold" color="white" fontSize="sm">JS</Flex>
                                <Box><Text fontSize="xs" color="gray.400">Primary Weapon</Text><Text fontSize="sm" fontWeight="bold" color="white">JavaScript</Text></Box>
                                <Box ml="auto"><svg width="20" height="20" viewBox="0 0 24 24" fill="none" stroke="#34d399" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round"><path d="M22 11.08V12a10 10 0 1 1-5.93-9.14" /><path d="m9 11 3 3L22 4" /></svg></Box>
                            </Flex>
                        </VStack>

                        {/* Community avatars */}
                        <Box position="relative" zIndex={10} mt="auto" pt={8}>
                            <Flex>
                                {['photo-1534528741775-53994a69daeb', 'photo-1506794778202-cad84cf45f1d', 'photo-1507003211169-0a1dd7228f2d'].map((id, i) => (
                                    <Image key={id} src={`https://images.unsplash.com/${id}?auto=format&fit=crop&w=64&h=64`} alt="" w={8} h={8} borderRadius="full" border="2px solid #0f172a" ml={i > 0 ? '-8px' : 0} objectFit="cover" />
                                ))}
                                <Flex w={8} h={8} borderRadius="full" border="2px solid #0f172a" bg="gray.600" align="center" justify="center" fontSize="10px" fontWeight="bold" color="white" ml="-8px">+2k</Flex>
                            </Flex>
                            <Text fontSize="xs" color="gray.500" mt={2}>Join the community today.</Text>
                        </Box>
                    </Box>

                    {/* Right Panel â€” Form */}
                    <Box flex={1} p={{ base: 8, md: 12 }} overflowY="auto">
                        <Box maxW="md" mx="auto">
                            <Box mb={8}>
                                <Heading fontFamily="heading" fontSize={{ base: '2xl', md: '3xl' }} fontWeight="bold" color="white" mb={2}>Create Your Profile</Heading>
                                <Text color="gray.400" fontSize="sm">Start competing. Rise through the ranks.</Text>
                            </Box>

                            <form onSubmit={handleSubmit}>
                                <VStack spacing={5}>
                                    {/* Username */}
                                    <Box w="100%">
                                        <Flex justify="space-between" align="center" ml={1} mb={1}>
                                            <Text fontSize="xs" fontWeight="semibold" color="gray.400" textTransform="uppercase" letterSpacing="wider">Username</Text>

                                            {usernameStatus.state === 'loading' && <Spinner size="xs" color="brand.500" />}
                                            {usernameStatus.state === 'available' && (
                                                <HStack spacing={1}>
                                                    <svg width="12" height="12" viewBox="0 0 24 24" fill="none" stroke="#22d3ee" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round"><path d="M20 6 9 17l-5-5" /></svg>
                                                    <Text fontSize="10px" fontFamily="mono" color="brand.500">Available</Text>
                                                </HStack>
                                            )}
                                            {usernameStatus.state === 'taken' && (
                                                <HStack spacing={1}>
                                                    <svg width="12" height="12" viewBox="0 0 24 24" fill="none" stroke="#f87171" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round"><circle cx="12" cy="12" r="10" /><line x1="15" y1="9" x2="9" y2="15" /><line x1="9" y1="9" x2="15" y2="15" /></svg>
                                                    <Text fontSize="10px" fontFamily="mono" color="red.400">Taken</Text>
                                                </HStack>
                                            )}
                                        </Flex>
                                        <InputGroup>
                                            <InputLeftElement pointerEvents="none" h="100%">
                                                <svg width="20" height="20" viewBox="0 0 24 24" fill="none" stroke="#64748b" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round"><circle cx="12" cy="12" r="4" /><path d="M16 8v5a3 3 0 0 0 6 0v-1a10 10 0 1 0-4 8" /></svg>
                                            </InputLeftElement>
                                            <Input type="text" placeholder="AlgoMaster99" value={username} onChange={(e) => setUsername(e.target.value)} {...inputStyles} />
                                        </InputGroup>
                                        <Text fontSize="10px" color="gray.500" ml={1} mt={1}>Your rank starts as: <Text as="span" color="yellow.500" fontWeight="bold">Rookie ðŸ¥‰</Text></Text>
                                    </Box>

                                    {/* Email */}
                                    <Box w="100%">
                                        <Flex justify="space-between" align="center" ml={1} mb={1}>
                                            <Text fontSize="xs" fontWeight="semibold" color="gray.400" textTransform="uppercase" letterSpacing="wider">Email Address</Text>

                                            {emailStatus.state === 'loading' && <Spinner size="xs" color="brand.500" />}
                                            {emailStatus.state === 'available' && (
                                                <HStack spacing={1}>
                                                    <svg width="12" height="12" viewBox="0 0 24 24" fill="none" stroke="#22d3ee" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round"><path d="M20 6 9 17l-5-5" /></svg>
                                                    <Text fontSize="10px" fontFamily="mono" color="brand.500">Available</Text>
                                                </HStack>
                                            )}
                                            {emailStatus.state === 'taken' && (
                                                <HStack spacing={1}>
                                                    <svg width="12" height="12" viewBox="0 0 24 24" fill="none" stroke="#f87171" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round"><circle cx="12" cy="12" r="10" /><line x1="15" y1="9" x2="9" y2="15" /><line x1="9" y1="9" x2="15" y2="15" /></svg>
                                                    <Text fontSize="10px" fontFamily="mono" color="red.400">In Use</Text>
                                                </HStack>
                                            )}
                                        </Flex>
                                        <InputGroup>
                                            <InputLeftElement pointerEvents="none" h="100%">
                                                <svg width="20" height="20" viewBox="0 0 24 24" fill="none" stroke="#64748b" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round"><rect width="20" height="16" x="2" y="4" rx="2" /><path d="m22 7-8.97 5.7a1.94 1.94 0 0 1-2.06 0L2 7" /></svg>
                                            </InputLeftElement>
                                            <Input type="email" placeholder="dev@example.com" value={email} onChange={(e) => setEmail(e.target.value)} {...inputStyles} />
                                        </InputGroup>
                                    </Box>

                                    {/* Password */}
                                    <Box w="100%">
                                        <Text fontSize="xs" fontWeight="semibold" color="gray.400" textTransform="uppercase" letterSpacing="wider" ml={1} mb={1}>Password</Text>
                                        <InputGroup>
                                            <InputLeftElement pointerEvents="none" h="100%">
                                                <svg width="20" height="20" viewBox="0 0 24 24" fill="none" stroke="#64748b" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round"><rect width="18" height="11" x="3" y="11" rx="2" ry="2" /><path d="M7 11V7a5 5 0 0 1 10 0v4" /></svg>
                                            </InputLeftElement>
                                            <Input type={showPassword ? 'text' : 'password'} placeholder="â€¢â€¢â€¢â€¢â€¢â€¢â€¢â€¢" value={password} onChange={(e) => setPassword(e.target.value)} {...inputStyles} pr="2.5rem" />
                                            <InputRightElement h="100%" right="1" display="flex" alignItems="center" justifyContent="center">
                                                <IconButton
                                                    variant="unstyled"
                                                    size="sm"
                                                    display="flex"
                                                    alignItems="center"
                                                    justifyContent="center"
                                                    onClick={() => setShowPassword(!showPassword)}
                                                    icon={showPassword ? <EyeOffIcon w={4} h={4} /> : <EyeIcon w={4} h={4} />}
                                                    color="gray.500"
                                                    _hover={{ color: 'gray.300' }}
                                                    aria-label={showPassword ? 'Hide password' : 'Show password'}
                                                />
                                            </InputRightElement>
                                        </InputGroup>
                                        <Flex mt={2} align="center" gap={2}>
                                            <Box flex={1} h="4px" bg="gray.600" borderRadius="full" overflow="hidden">
                                                <Box h="100%" w={strength.w} bgGradient="linear(to-r, red.500, yellow.500, green.500)" borderRadius="full" transition="width 0.3s" />
                                            </Box>
                                            <Text fontSize="10px" fontFamily="mono" color={strength.c}>{strength.l}</Text>
                                        </Flex>
                                    </Box>



                                    {/* reCAPTCHA Widget */}
                                    <Box w="100%" display="flex" flexDirection="column" alignItems="center" mt={2} mb={2}>
                                        <ReCAPTCHA
                                            ref={recaptchaRef}
                                            sitekey="6LdKIHMsAAAAACo6AkNg2KChjBhGcVCj2Rwj-rey"
                                            onChange={(token) => { setRecaptchaToken(token); setErrorMsg(''); }}
                                            theme="dark"
                                        />
                                        {errorMsg && <Text fontSize="xs" color="red.400" mt={2}>{errorMsg}</Text>}
                                    </Box>

                                    {/* Submit */}
                                    <Box pt={4} w="100%">
                                        <Button type="submit" w="100%" h="48px" bgGradient="linear(to-r, brand.500, cyan.400)" color="#0f172a" fontSize="sm" fontWeight="bold" borderRadius="8px"
                                            isDisabled={isFormInvalid}
                                            isLoading={isLoading} loadingText="Creating Profile..." boxShadow="0 0 30px -5px rgba(34,211,238,0.5)" position="relative" overflow="hidden" role="group"
                                            _hover={{ bgGradient: 'linear(to-r, brand.500, cyan.300)', transform: 'translateY(-2px)' }} _active={{ transform: 'translateY(0)' }} transition="all 0.3s">
                                            <Box position="absolute" inset={0} bg="whiteAlpha.200" transform="translateX(-100%) skewX(-12deg)" _groupHover={{ transform: 'translateX(100%) skewX(-12deg)' }} transition="transform 0.5s" />
                                            <HStack spacing={2} position="relative">
                                                <Text>Enter the Arena</Text>
                                                <Box _groupHover={{ transform: 'translateX(4px)' }} transition="transform 0.3s">
                                                    <svg width="16" height="16" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round"><path d="M5 12h14" /><path d="m12 5 7 7-7 7" /></svg>
                                                </Box>
                                            </HStack>
                                        </Button>
                                    </Box>
                                </VStack>
                            </form>

                            <Text mt={6} textAlign="center" fontSize="sm" color="gray.400">
                                Already have an account?{' '}
                                <Link as={RouterLink} to="/signin" fontWeight="medium" color="brand.500" _hover={{ color: 'brand.300' }}>Sign in</Link>
                            </Text>
                        </Box>
                    </Box>
                </Box>
            </MotionBox>

            <style>{`@keyframes ping { 75%, 100% { transform: scale(2); opacity: 0; } }`}</style>
        </AuthLayout>
    );
};

export default SignUp;
